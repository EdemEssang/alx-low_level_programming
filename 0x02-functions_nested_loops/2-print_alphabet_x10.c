#include <stdio.h>

/**
 *
 * A function that prints 10 times the alphabet, in lowercase,
 * followed by a new line.
 *
 */


void print_alphabet_x10(void)
{

	int n, counter;

	counter = 0;

	while (counter < 10)
	{
	
		for (n = 'a'; n <= 'z'; n++)
		{
		
			_putchar(n);
		
		}
	
		count++;
		  _putchar('\n');
	
	
	
	
	}	






}
